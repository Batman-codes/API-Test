package reqpojos;

public class CreateUserRequest {

	private String name;
	private String job;
	
	public String getName() {
		return name;
	}
	public void setName(String user) {
		this.name = user;
	}
	public String getJob() {
		return job;
	}
	public void setJob(String job) {
		this.job = job;
	}
	
	
}
